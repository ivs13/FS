The Oracle Messaging Gateway integrates Oracle Database applications with other
message queuing systems, such as Websphere MQ (formerly called MQ Series) and 
Tibco. Because many legacy applications on mainframes communicate with Websphere
MQ, there is a need for integrating these applications into an Oracle Database 
environment. The message gateway makes non-Oracle message queues appear as if 
they were Oracle Streams queues, and automatically propagates messages between 
Oracle Streams queues and Websphere MQ or Tibco queues.


